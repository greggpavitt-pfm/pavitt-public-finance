-- Migration: Pre-computed RAG Knowledge Cache
-- IPSAS Practitioner Advisor — offline-generated context packets
--
-- Run this in Supabase SQL Editor AFTER migration-openrouter-config.sql.
--
-- What this adds:
--   1. rag_knowledge_cache      — one row per IPSAS standard or topic cluster;
--                                 stores pre-retrieved ChromaDB chunks so that
--                                 query time hits Supabase instead of ChromaDB
--   2. rag_cache_invalidation_log — manual audit trail for cache rebuild events
--   3. rag_cache_build_log        — records each run of the build script
